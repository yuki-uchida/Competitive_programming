def binary_search(num_list, num, left, right):
    mid = (left + right) // 2
    # print(left, mid, right)
    if num_list[mid] == num:
        # print(f"{num}がある")
        return True
    if left >= right:
        return False
    elif num_list[mid] > num:
        return binary_search(num_list, num, left, mid - 1)
    else:
        return binary_search(num_list, num, mid + 1, right)


